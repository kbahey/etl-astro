# Convert object type from abbreviation to a number
def get_obj_type(obj_type = ''):
  return object_type[obj_type]
  
# Convert constellation from abbreviation to a number
def get_constellation(abbr = ''):
  return constellation[abbr]

# Convert Bayer designation from abbreviation to a number
def get_bayer(abbr = ''):
  s = abbr.split('-')[0]
  greek = s.strip(' ')
  try:
    designation = bayer[greek]
  except KeyError:
    return False, ''

  return True, designation

# Lookup a Herschel object in a list
def herschel(name):
  for o in herschel_list:
    full_name = 'NGC' + str(o).zfill(4)
    if full_name == str(name):
      return o

  return False

# Lookup a Caldwell object in a list
def caldwell(full_name):
  for o in caldwell_list:
    if o == full_name:
      return caldwell_list[o]

  return False
